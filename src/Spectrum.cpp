void Spectrum::FFT(int16_t fr[], int16_t fi[], int16_t m)
{
  const int32_t n = 1 << m;

  /* max FFT size = N_WAVE */
  if (n > N_WAVE) return;

  int32_t mr = 0;
  int32_t nn = n - 1;

  /* decimation in time - re-order data */
  for (m=1; m<=nn; ++m)
  {
    int32_t l = n;
    do
    {
      l >>= 1;
    } while (mr+l > nn);
    mr = (mr & (l-1)) + l;

    if (mr <= m)
      continue;

    const int16_t tr = fr[m];
    fr[m] = fr[mr];
    fr[mr] = tr;
    const int16_t ti = fi[m];
    fi[m] = fi[mr];
    fi[mr] = ti;
  }

  int32_t l = 1;
  int32_t k = LOG2_N_WAVE-1;
  while (l < n)
  {
    const int32_t istep = l << 1;
    for (m=0; m<l; ++m)
    {
      const int32_t j = m << k;
      /* 0 <= j < N_WAVE/2 */
      int16_t wr =  Sinewave[j+N_WAVE/4];
      int16_t wi = -Sinewave[j];
      wr >>= 1;
      wi >>= 1;
      for (int32_t i=m; i<n; i+=istep)
      {
        const int32_t j = i + l;
        const int16_t tr = FIX_MPY(wr,fr[j]) - FIX_MPY(wi,fi[j]);
        const int16_t ti = FIX_MPY(wr,fi[j]) + FIX_MPY(wi,fr[j]);
        const int16_t qr = fr[i]>>1;
        const int16_t qi = fi[i]>>1;
        fr[j] = qr - tr;
        fi[j] = qi - ti;
        fr[i] = qr + tr;
        fi[i] = qi + ti;
      }
    }
    k--;
    l = istep;
  }
}

